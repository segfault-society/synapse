-- ============================================================================
-- RBAC (Role-Based Access Control) Migration
-- ============================================================================
-- This migration implements a secure, flexible RBAC system using Supabase's
-- recommended Custom Access Token Auth Hook approach.
-- 
-- SECURITY NOTES:
-- 1. Roles are stored in JWT claims (cannot be tampered with client-side)
-- 2. RLS policies use the authorize() function to check permissions
-- 3. Only supabase_auth_admin can read/modify user_roles (not even authenticated users)
-- 4. The auth hook runs server-side before token issuance
-- ============================================================================

-- ============================================================================
-- STEP 1: Create custom types for roles and permissions
-- ============================================================================
-- Using ENUMs ensures type safety and prevents invalid values
-- Add new roles/permissions here as your app grows

create type public.app_role as enum ('user', 'moderator', 'admin');
create type public.app_permission as enum (
  -- User management permissions
  'users.read',
  'users.update',
  'users.delete',
  -- Items management permissions  
  'items.read_all',
  'items.update_all',
  'items.delete_all',
  -- Admin permissions
  'admin.access',
  'admin.manage_roles'
);

-- ============================================================================
-- STEP 2: Create user_roles table
-- ============================================================================
-- Tracks which roles are assigned to which users
-- A user can have multiple roles (though typically just one)

create table public.user_roles (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete cascade not null,
  role app_role not null default 'user',
  created_at timestamptz default now() not null,
  created_by uuid references auth.users(id),
  unique (user_id, role)
);

comment on table public.user_roles is 'Application roles assigned to users. Managed by admins only.';

-- Index for faster lookups
create index user_roles_user_id_idx on public.user_roles(user_id);
create index idx_user_roles_created_by on public.user_roles(created_by);

-- ============================================================================
-- STEP 3: Create role_permissions table
-- ============================================================================
-- Maps roles to their permissions
-- This is a configuration table - modify as needed for your app

create table public.role_permissions (
  id bigint generated by default as identity primary key,
  role app_role not null,
  permission app_permission not null,
  unique (role, permission)
);

comment on table public.role_permissions is 'Permissions granted to each role.';

